int main(){
    int t;
    cin>>t;
    while(t--){
        int n;
        cin>>n;
        int l1=0,r1=0,r0=0,l0=0;
        string s;
        cin>>s;
        for(int i=0;i<s.size();i++){
            if(s[i]=='0'){
                r0++;
            }
            else{
                r1++;
            }
        }
        int dist=INT_MAX,ans=-1;
        if(r0<=r1){
            dist=n;
            ans=0;
        }
        for(int i=0;i<n;i++){
            if(s[i]=='0'){
                r0--;
                l0++;
            }
            else{
                r1--;
                l1++;
            }
            int left=l1+l0;
            int right=r1+r0;
            if(l0>=l1 && r1>=r0){
                int x=abs(right-left);
                if(x<dist){
                    dist=x;
                    ans=i+1;
                }
            }
        }
        cout<<ans<<endl;
    }
    return 0;
}